package com.eomcs.oop.ex02.util;

public class Calculator {

	public int result = 0;

	   public void plus(int value) {
	     result += value;
	    }

	   public void minus(int value) {
	      result -= value;
	    }

	    public void multiple(int value) {
	      result *= value;
	    }

	    public void divide(int value) {
	      result /= value;
	    }

}
