package com.eomcs.basic.ex05;

public class Ex430 {
	public static void main(String[] args) {
		
		int i = 105;	// 01101001
		
		System.out.println(i);		// 105
		
		System.out.println(i >> 1);	// 52
		
		System.out.println(i >> 2); // 26
		
		System.out.println(i >> 3);	// 13	
		
		System.out.println(i >> 4); // 6
	}

}
